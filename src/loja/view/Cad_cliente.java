    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
        Cliente_model c = new Cliente_model();

        c.setIdCli(Integer.parseInt(jTextField1.getText()));
        c.setNomeCli(jTextField2.getText());
        c.setFoneCli(jFormattedTextField2.getText());
        c.setEmailCli(jTextField3.getText());

        DateFormat formatador = new SimpleDateFormat("dd/MM/yyyy");
        c.setDataNascCli(new java.sql.Date(((java.util.Date) formatador.parse(jFormattedTextField1.getText())).getTime()));

        Cliente_CRUD cd = new Cliente_CRUD();
        cd.salvar(c);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jFormattedTextField1.setText("");
        jFormattedTextField2.setText("");

        JOptionPane.showMessageDialog(null, "Operação realizada com sucesso.", "Cadastro de Clientes", JOptionPane.INFORMATION_MESSAGE);

     } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
        catch(Exception e){
              JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

